import request from '@/utils/request'

export function fetchList(query) {
  return request({
    url: '/family-tree-book/list',
    method: 'get',
    params: query
  })
}

export function createFamilyTreeBook(data) {
  return request({
    url: '/family-tree-book/create',
    method: 'post',
    data
  })
}

export function deleteFamilyTreeBook(data) {
  return request({
    url: '/family-tree-book/delete',
    method: 'post',
    data
  })
}
