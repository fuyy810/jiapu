import request from '@/utils/request'

export function fetchList(query) {
  return request({
    url: '/family-article/list',
    method: 'get',
    params: query
  })
}

export function detailFamilyArticle(query) {
  return request({
    url: '/family-article/detail',
    method: 'get',
    params: query
  })
}

export function createFamilyArticle(data) {
  return request({
    url: '/family-article/create',
    method: 'post',
    data
  })
}

export function updateFamilyArticle(data) {
  return request({
    url: '/family-article/update',
    method: 'post',
    data
  })
}

export function deleteFamilyArticle(data) {
  return request({
    url: '/family-article/delete',
    method: 'post',
    data
  })
}
